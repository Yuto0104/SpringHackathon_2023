//=============================================================================
// インスタンス生成
// Author : 唐﨑結斗
// 概要 : 2Dバレットを生成する
//=============================================================================
CLille * CLille::Create()
{
	// オブジェクトインスタンス
	CLille *pLille = nullptr;

	pLille = new CLille;

	// メモリの確保ができなかった
	assert(pLille != nullptr);

	// 数値の初期化
	pLille->Init();

	// インスタンスを返す
	return pLille;
}

//=============================================================================
// コンストラクタ
// Author : 唐﨑結斗
// 概要 : インスタンス生成時に行う処理
//=============================================================================
