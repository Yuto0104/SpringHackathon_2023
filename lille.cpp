//=============================================================================
// 抽選
// Author : 唐﨑結斗
// 概要 : リールの抽選を行う
//=============================================================================
bool CLille::Lottery()
{
	// 結果
	bool bResult = false;

	if (CCalculation::Rand(100, 1) < m_nRatio)
	{// 当たった
		bResult = true;
	}

	return bResult;
}


