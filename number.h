#include "object2D.h"

//=============================================================================
// 2Dナンバークラス
// Author : 唐﨑結斗
// 概要 : 2Dナンバー生成を行うクラス
//=============================================================================
class CNumber : public CObject2D
{
public:
	//--------------------------------------------------------------------
	// 静的メンバ関数
	//--------------------------------------------------------------------
	// 2Dナンバーの生成
	static CNumber *Create();

	//--------------------------------------------------------------------
	// コンストラクタとデストラクタ
	//--------------------------------------------------------------------
	explicit CNumber(int nPriority = CObject::PRIORITY_LEVEL3);
	~CNumber() override;

	//--------------------------------------------------------------------
	// メンバ関数
	//--------------------------------------------------------------------
	HRESULT Init() override;				// 初期化
	void Uninit() override;					// 終了
	void Update() override;					// 更新
	void Draw() override;					// 描画
	void SetNumber(int nNumber);			// ナンバーの設定
	int GetNumber() { return m_nNumber; }	// ナンバーの取得

private:
	//--------------------------------------------------------------------
	// メンバ変数
	//--------------------------------------------------------------------
	int m_nNumber;		// 数値
};

#endif
