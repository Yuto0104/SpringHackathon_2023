#ifndef _LIGHT_H_			// このマクロ定義がされてなかったら
#define _LIGHT_H_			// 二重インクルード防止のマクロ定義

//***************************************************************************
// インクルード
//***************************************************************************
#include"main.h"

//*****************************************************************************
// マクロ定義
//*****************************************************************************
static const unsigned int MAX_LIGHT = (0x100);

//=============================================================================
// ライトクラス
// Author : 唐﨑結斗
// 概要 : ライト設定を行うクラス
//=============================================================================
class CLight
{
public:
	//--------------------------------------------------------------------
	// 静的メンバ関数
	//--------------------------------------------------------------------
	static CLight *Create(D3DXVECTOR3 vec, D3DXCOLOR col);					// ライトの生成
	static void ReleaseAll(void);											// 解放

	//--------------------------------------------------------------------
	// コンストラクタとデストラクタ
	//--------------------------------------------------------------------
	CLight();
	~CLight();

	//--------------------------------------------------------------------
	// メンバ関数
	//--------------------------------------------------------------------
	void Init(void);										// 初期化
	void Uninit(void);										// 終了
	void Update(void);										// 更新
	void SetVec(D3DXVECTOR3 vec) { m_vecDir = vec; }		// 方向ベクトルの設定
	void SetCol(D3DXCOLOR color) { m_color = color; }		// ライトカラーの設定
	void Release(void);										// インスタンスの解放
	D3DXVECTOR3 GetLightVec() { return m_vecDir; }			// ライト方向のゲット

private:
	//--------------------------------------------------------------------
	// 静的メンバ変数
	//--------------------------------------------------------------------
	static CLight *m_apLight[MAX_LIGHT];				// ライト情報

	//--------------------------------------------------------------------
	// メンバ変数
	//--------------------------------------------------------------------
	D3DLIGHT9		m_light;			// ライト情報
	D3DXVECTOR3		m_vecDir;			// ライト方向ベクトル
	D3DXCOLOR		m_color;			// カラー
	int				m_nNumID;			// 格納先のID
};

#endif
