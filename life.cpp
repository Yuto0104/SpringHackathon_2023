//=============================================================================
// ライフの初期化
// Author : 髙野馨將
// 概要 : 頂点バッファを生成し、メンバ変数の初期値を設定
//=============================================================================
HRESULT CLife::Init()
{// オブジェクト2Dの初期化処理
	CObject2D::Init();
	//初期化
	SetCol(D3DXCOLOR(0.0f,1.0f,0.0f,1.0f));
	SetPos(D3DXVECTOR3(50.0f, 0.0f, 0.0f));

	return S_OK;
}

