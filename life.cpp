// 概要 : 2Dオブジェクトを生成する
//=============================================================================
CLife * CLife::Create(void)
{
	// オブジェクトインスタンス
	CLife *pObject2D = nullptr;

	// メモリの解放
	pObject2D = new CLife;

	if (pObject2D != nullptr)
	{// 数値の初期化
		pObject2D->Init();
	}
	else
	{// メモリの確保ができなかった
		assert(false);
	}

	// インスタンスを返す
	return pObject2D;
}

